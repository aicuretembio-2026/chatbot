// 현재 언어
let currentChatbotLang = 'ko';

// 언어 변경 함수
function changeChatbotLanguage(lang) {
    currentChatbotLang = lang;
    
    // UI 텍스트 업데이트 (먼저 실행)
    updateChatbotUI(lang);
    
    // 챗봇 데이터 로드 (이후 버튼 생성)
    loadChatbotData(lang);
    
    // 언어 선택 버튼 업데이트
    updateLanguageButton(lang);
}

// 챗봇 데이터 로드 함수 (HTML에서 사용)
function loadChatbotData(lang) {
    // 이 함수는 HTML 파일에서 정의됨
    console.log(`챗봇 데이터 로드 요청: ${lang}`);
}

// UI 텍스트 업데이트
function updateChatbotUI(lang) {
    const translations = CHATBOT_I18N[lang] || CHATBOT_I18N['ko'];
    
    // data-i18n 속성이 있는 모든 요소 업데이트
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[key]) {
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[key];
            } else {
                element.innerHTML = translations[key];
            }
        }
    });
    
    // 페이지 타이틀 업데이트
    if (translations.page_title) {
        document.title = `${translations.page_title} - ${translations.page_subtitle}`;
    }
    
    // 빠른 답변 버튼 업데이트
    updateQuickReplyButtons();
}

// 빠른 답변 버튼 동적 생성
function updateQuickReplyButtons() {
    const container = document.getElementById('keywordsContainer');
    if (!container || !currentChatbotData || !currentChatbotData.quickReplies) {
        return;
    }
    
    // 기존 버튼 제거
    container.innerHTML = '';
    
    // 새 버튼 생성
    currentChatbotData.quickReplies.forEach(reply => {
        const button = document.createElement('button');
        button.className = 'quick-reply';
        button.setAttribute('data-query', reply.query);
        button.textContent = reply.text;
        container.appendChild(button);
    });
    
    console.log(`✅ ${currentChatbotData.quickReplies.length}개 버튼 생성 완료`);
}

// 언어 선택 버튼 업데이트
function updateLanguageButton(lang) {
    const languageNames = {
        'ko': '한국어',
        'en': 'English',
        'ja': '日本語',
        'es': 'Español',
        'fr': 'Français',
        'th': 'ไทย',
        'vi': 'Tiếng Việt',
        'id': 'Indonesia',
        'ms': 'Bahasa Melayu',
        'zh-TW': '繁體中文'
    };
    
    const button = document.querySelector('.language-button span');
    if (button) {
        button.textContent = languageNames[lang] || '한국어';
    }
}

// 페이지 로드 시 초기화
document.addEventListener('DOMContentLoaded', () => {
    // URL에서 언어 파라미터 확인
    const urlParams = new URLSearchParams(window.location.search);
    const urlLang = urlParams.get('lang');
    
    // 저장된 언어 또는 URL 언어 또는 기본 언어
    const savedLang = localStorage.getItem('chatbotLanguage') || urlLang || 'ko';
    
    // 초기 언어 설정
    changeChatbotLanguage(savedLang);
});
